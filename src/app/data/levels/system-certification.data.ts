// ---------------------------------------------------------------------------
// Builder helpers (private to this file)
// ---------------------------------------------------------------------------

/** Build a SourceCodeLine. */
function line(lineNumber: number, content: string, isTestable: boolean, isBranch = false): SourceCodeLine {
  return { lineNumber, content, isTestable, isBranch };
}

/** Build a SourceCodeBlock, auto-deriving testablePoints and branchPoints from lines. */
function sourceBlock(lines: SourceCodeLine[]): SourceCodeBlock {
  return {
    lines,
    testablePoints: lines.filter(l => l.isTestable).map(l => l.lineNumber),
    branchPoints: lines.filter(l => l.isBranch).map(l => l.lineNumber),
  };
}

/** Build a CertificationThreshold. */
function threshold(minCoverage: number, timeLimit: number, maxRedundantTests: number): CertificationThreshold {
  return { minCoverage, timeLimit, maxRedundantTests };
}

/** Build a CertificationHint. */
function hint(order: number, uncoveredLineNumber: number, text: string): CertificationHint {
  return { order, uncoveredLineNumber, text };
}

